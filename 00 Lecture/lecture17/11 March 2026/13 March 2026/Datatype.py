# type() -> check datatype of value

data = 23
print(type(data))

data = "codeswithpankaj.com"
print(type(data))

data = 2.3
print(type(data))

data = True
print(type(data))


data = 23,
print(type(data))

data = (23,)
print(type(data))

data = (23,45)
print(type(data))

data = [23,24,25,26,27]
print(type(data))

data = {34,34,35}
print(type(data))

data = {'name':'Nishant Jain','Age':25}
print(type(data))
