# Arithmetic Operators
x = 567
y = 13

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x//y)
print(x%y)
print(x**2)

# Comparison Operators 
print(x < y)
print(x > y)
print(x == y)
print(x != y)
print(x <= y)
print(x >= y)

# Logical Operators

print(56 > 90 and 67 != 78)
print(56 > 90 or 67 != 78)

print(not 23 == 23)

print(23 != 23)

