# Arithmetic Operators
x = 129
y = 78

print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x//y)
print(x%y)
print(x**20)

# Comparison Operators

print(x < y)
print(x >= y)
print(x <= y)
print(x > y)
print(x == y)
print(x != y)

# logical operators
print(10 < 90 and 56 == 57)
print(10 < 90 or 56 == 57)

print(not 10 == 10)
print(10 != 10)

# Assignment Operators

