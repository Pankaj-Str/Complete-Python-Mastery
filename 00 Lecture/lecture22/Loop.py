# range(end) # start from 0
for i in range(10):
    print(i)
print("---------------------")
# range(start,end)
for i in range(55,65):
    print(i)
print("----------------------")
# range(start,end,step)
for i in range(1,20,3):
    print(i)

# end=''
for i in range(1,10):
    print(i,end=' | ')
# using if into loop
for i in range(1,10):
    if i == 5:
        print(i,'Best Number')
    else:
        print(i)        






    
